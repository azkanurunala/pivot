// gen-gift-codes.js — regenerate giftcodes.js (hashes, committed) + GIFT_CODES.txt
// (plaintext, gitignored). Run: node scripts/gen-gift-codes.js [count]
// The app only ever sees the hashes, so the plaintext codes can't be reverse-
// engineered from the shipped bundle.

const fs = require('fs');
const path = require('path');

const COUNT = parseInt(process.argv[2] || '100', 10);
const ALPHA = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789'; // no ambiguous chars

function djb2(s) {
  let h = 5381;
  for (let i = 0; i < s.length; i++) h = ((h << 5) + h + s.charCodeAt(i)) >>> 0;
  return h >>> 0;
}
function block(n) {
  let s = '';
  for (let i = 0; i < n; i++) s += ALPHA[Math.floor(Math.random() * ALPHA.length)];
  return s;
}
const codes = new Set();
while (codes.size < COUNT) codes.add(`PIVOT-${block(4)}-${block(4)}`);

const list = [...codes];
const hashes = list.map((c) => djb2(c.toUpperCase()));

const js = `// giftcodes.js — AUTO-GENERATED by scripts/gen-gift-codes.js. Do not edit by hand.
const HASHES = new Set([${hashes.join(', ')}]);

function djb2(s) {
  let h = 5381;
  for (let i = 0; i < s.length; i++) h = ((h << 5) + h + s.charCodeAt(i)) >>> 0;
  return h >>> 0;
}

export function isValidGiftCode(code) {
  if (!code) return false;
  return HASHES.has(djb2(String(code).trim().toUpperCase()));
}
`;
fs.writeFileSync(path.join(__dirname, '..', 'src', 'giftcodes.js'), js);
fs.writeFileSync(path.join(__dirname, '..', 'GIFT_CODES.txt'), list.join('\n') + '\n');
console.log(`Wrote ${list.length} codes → src/giftcodes.js (hashes) + GIFT_CODES.txt (plaintext, gitignored)`);
